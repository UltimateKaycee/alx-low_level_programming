#include <stdio.h>
/**
*main - Entry point
*Description: "Print some strings with a new line"
*Return: Always 0 (success)
*/
int main (void)
{
    printf("The size of type int is %d:", sizeof(int));
    printf("The size of type char is %c:", sizeof(char));
    printf("The size of type long int is %d:", sizeof(long int));
    printf("The size of type long long int is %d:", sizeof(long long int));
    printf("The size of type float is %f:", sizeof(int));
}

