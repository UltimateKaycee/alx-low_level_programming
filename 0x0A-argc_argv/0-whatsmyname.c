#include <stdio.h>

/**
 * main - function prints program name, followed by new line.
 * @argc: number of arguments in the program.
 * @argv: array of pointers to arguments.
 *
 * Return: Always o.
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
